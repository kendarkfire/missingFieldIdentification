package com.trellisconnect.missingFieldIdentification.domain;

public class Address {
	private String number;
	private String street;
	private String type;
	private String sec_unit_type;
	private String sec_unit_num;
	private String suffix;
	private String city;
	private String state;
	private String zip;

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSec_unit_type() {
		return sec_unit_type;
	}

	public void setSec_unit_type(String sec_unit_type) {
		this.sec_unit_type = sec_unit_type;
	}

	public String getSec_unit_num() {
		return sec_unit_num;
	}

	public void setSec_unit_num(String sec_unit_num) {
		this.sec_unit_num = sec_unit_num;
	}

	public String getSuffix() {
		return suffix;
	}

	public void setSuffix(String suffix) {
		this.suffix = suffix;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	

}
